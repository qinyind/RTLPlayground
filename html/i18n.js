var rtlLang = (function() {
  var saved = localStorage.getItem('rtl_lang');
  if (saved && LANG[saved]) return saved;
  var browser = (navigator.language || navigator.userLanguage || 'en').substring(0, 2);
  return LANG[browser] ? browser : 'en';
})();

document.documentElement.lang = rtlLang;

function t(key) {
  return LANG[rtlLang][key] || LANG['en'][key] || key;
}

function setLang(lang) {
  if (LANG[lang]) {
    localStorage.setItem('rtl_lang', lang);
    rtlLang = lang;
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n], [data-i18n-placeholder], [data-i18n-aria-label]').forEach(function(el) {
      applyTranslation(el);
    });
  }
}

function applyTranslation(el) {
  var placeholderKey = el.getAttribute('data-i18n-placeholder');
  var ariaLabelKey = el.getAttribute('data-i18n-aria-label');
  if (placeholderKey) el.setAttribute('placeholder', t(placeholderKey));
  if (ariaLabelKey) el.setAttribute('aria-label', t(ariaLabelKey));
  var key = el.getAttribute('data-i18n');
  if (!key) return;
  if (el.tagName === 'INPUT' && (el.type === 'submit' || el.type === 'button')) {
    el.value = t(key);
  } else if (el.tagName === 'OPTION') {
    el.textContent = t(key);
  } else if (el.tagName === 'TITLE') {
    el.textContent = t(key);
  } else {
    el.innerHTML = t(key);
  }
}

document.addEventListener('DOMContentLoaded', function() {
  document.querySelectorAll('[data-i18n], [data-i18n-placeholder], [data-i18n-aria-label]').forEach(function(el) {
    applyTranslation(el);
  });
});
